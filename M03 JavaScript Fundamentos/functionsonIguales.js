function sonIguales(x, y) {
   // Retorna true si "x" e "y" son iguales.
   // De lo contrario, retorna false.
   // Tu código:
   return x === y;

}
// Ejemplo 
const resultado1 = sonIguales(3, 3); // Compara si 3 es igual a 3
console.log(resultado1); // Esto imprimirá true 

const resultado2 = sonIguales(5, 3); // Compara si 5 es igual a 3
